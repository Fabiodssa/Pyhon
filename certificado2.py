numero1 = float(input("Digite o 1º numero: "))
numero2 = float(input("Digite o 2º numero: "))
numero3 = float(input("Digite o 3º numero: "))

numeromaior=max(numero1,numero2,numero3)
print("o maior numero é: ",numeromaior)

numeromenor=min(numero1,numero2,numero3)
print("o menor numero é: ",numeromenor)